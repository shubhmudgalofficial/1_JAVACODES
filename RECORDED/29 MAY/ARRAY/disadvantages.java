
public class disadvantages {
    public static void main(String[] args) {
        int nums[]={23,45,3,24,4};
        System.out.println(nums.length);    
        // Disadvantages of arrays
        // 1. Fixed Size: Arrays have a fixed size, which means you need to know the size in advance and cannot change it dynamically.
        // 2. Inefficient Insertion/Deletion: Inserting or deleting elements in an array requires shifting elements, which can be inefficient for large arrays.
        // 3. Lack of Flexibility: Arrays cannot easily be resized or modified once created, requiring the creation of a new array with the desired size.
        // 4. Wasted Memory: Arrays allocate memory for the maximum number of elements they can hold, even if the actual number of elements is smaller.
        // 5. Limited Data Types: Arrays can only store elements of the same data type, making it less flexible for storing different types of data.

        // Example code to demonstrate the disadvantages of arrays
        System.out.println("Disadvantages of arrays:");
        System.out.println("1. Fixed Size");
        System.out.println("2. Inefficient Insertion/Deletion");
        System.out.println("3. Lack of Flexibility");
        System.out.println("4. Wasted Memory");
        System.out.println("5. Limited Data Types");
        
    }
}
