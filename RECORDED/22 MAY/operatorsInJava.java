public class operatorsInJava {
    public static void main(String[] args) {
        // arithmetic operators
        System.out.println(10+20); //additional operator
        int num1=10,num2=50;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num2%num1);
    }
}
