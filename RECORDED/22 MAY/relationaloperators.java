public class relationaloperators {
    public static void main(String[] args) {
        int a ,b=10,c=20;
        a=b;
        System.out.println(a==b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a>c);
        System.out.println(a<c);
        System.out.println(a!=c);
        System.out.println(a=c);
    }
}
