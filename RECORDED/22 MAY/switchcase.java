class switchcase {
    public static void main(String[] args) {
        int num = 100;
        switch (num) {
            case 200:
                System.out.println("case1");
                break;
            case 100:
                System.out.println("case2");
                break;
            case 300:
                System.out.println("case 3");
                break;
            default:
                System.out.println("default case");

        }
    }
}