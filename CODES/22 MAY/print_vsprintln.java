public class print_vsprintln {
    public static void main(String[] args) {
        System.out.println("*");
        System.out.println("*");
        System.out.println("*");// next line cursor shift after printing
        System.out.print("$");
        System.out.print("$");
        System.out.print("$");// same line cursor shift after printing
    }
}
