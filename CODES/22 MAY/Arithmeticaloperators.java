public class Arithmeticaloperators {
public static void main(String[] args) {
    System.out.println(20+10);
    int a=10,b=20;
    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);
    System.out.println(a%b);
    System.out.println(b+a);
    System.out.println(b-a);
    System.out.println(b*a);
    System.out.println(b/a);
    System.out.println(b%a);


}
}
